public class RelationalOperation {
        public static void main(String args[]) {
                int a = 3;
                int b = 5;
                System.out.println("a == b = " + (a == b));
                System.out.println("a != b = " + (a != b));
                System.out.println("a > b = " + (a > b));
                System.out.println("a < b = " + (a < b));
                System.out.println("a >= b = " + (a >= b));
                System.out.println("a <= b = " + (a <= b));
                System.out.println("a > b ? a : b = " + (a > b ? a : b));
        }
}
